# Operators and operands

# Arithmetic Operators

print(20 + 10)
print(20 - 10)
print(20 * 10)
print(20 / 15)

print(20 // 15)
print(20 % 10)
print(2 ** 3)

# comparison operators

print( 3 > 6)
print( 3 >= 6)
print( 3 < 6)
print( 3 <= 6)
print(3 == 6)
print(3 != 6)

# Logical Operators

print(3 > 6 and 3 < 6)
print(3 > 6 or 3 < 6)
print(not(3 == 6))